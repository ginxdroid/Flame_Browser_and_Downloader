package com.ginxdroid.flamebrowseranddownloader.models;

public class HomePageItem {

    private String HpFaviconPath;
    private String HpTitle;
    private String HpURL;

    public HomePageItem() {
    }

    public String getHpFaviconPath() {
        return HpFaviconPath;
    }

    public void setHpFaviconPath(String hpFaviconPath) {
        HpFaviconPath = hpFaviconPath;
    }

    public String getHpTitle() {
        return HpTitle;
    }

    public void setHpTitle(String hpTitle) {
        HpTitle = hpTitle;
    }

    public String getHpURL() {
        return HpURL;
    }

    public void setHpURL(String hpURL) {
        HpURL = hpURL;
    }
}
