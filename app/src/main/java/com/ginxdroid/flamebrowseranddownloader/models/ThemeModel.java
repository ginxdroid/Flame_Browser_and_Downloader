package com.ginxdroid.flamebrowseranddownloader.models;

public class ThemeModel {

    private String themeAccentColor;
    private String themeAccentTextColor;

    public ThemeModel() {
    }

    public String getThemeAccentColor() {
        return themeAccentColor;
    }

    public void setThemeAccentColor(String themeAccentColor) {
        this.themeAccentColor = themeAccentColor;
    }

    public String getThemeAccentTextColor() {
        return themeAccentTextColor;
    }

    public void setThemeAccentTextColor(String themeAccentTextColor) {
        this.themeAccentTextColor = themeAccentTextColor;
    }
}
